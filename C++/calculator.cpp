#include <iostream>

int main()
{
    int x, y;
    x = 5;
    y = 6;

    std::cout << "addition: " << x + y << '\n';
    std::cout << "subtraction: " << x - y << '\n';
    std::cout << "multiplication: " << x * y << '\n';
    std::cout << "divition: " << x / y << '\n';
    std::cout << "remainder: " << x % y << '\n';

    return 0;
}